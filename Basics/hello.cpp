#include <iostream>
using namespace std;

int main() {
    
    // print
    cout<<"Hello World";

    // end line
    cout<<endl;
    
    // single comment

    /*
    multiline comment
    */

    // print numbers
    cout<<3<<endl;

    // print sum
    cout<<4+5<<endl;

    // new line
    cout<<"Hey, Baby! \n";
    cout<<6;

    // blank line
    cout<<"\n\n";
    cout<<"blank line above"<<endl;

    // how to write a tab, slash and comma
    cout<<"\t"<<"\\"<<"\"";

    return 0;
}